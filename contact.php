        $(document).ready(function() {
            $('#contactForm').on('submit', function(e) {
                e.preventDefault();

                var formData = $(this).serialize();
                var messageDiv = $('#formMessage');
                var submitBtn = $(this).find('button[type="submit"]');

                // Disable submit button and show loading
                submitBtn.prop('disabled', true).html('<i class="fa fa-spinner fa-spin"></i> Sending...');
                messageDiv.html('');

                $.ajax({
                    url: 'contact-form-handler.php',
                    type: 'POST',
                    data: formData,
                    dataType: 'json',
                    success: function(data) {
                        if (data.status === 'success') {
                            messageDiv.html('<div class="alert alert-success">' + data.message + '</div>');
                            $('#contactForm')[0].reset();
                        } else {
                            messageDiv.html('<div class="alert alert-danger">' + data.message + '</div>');
                        }
                    },
                    error: function() {
                        messageDiv.html('<div class="alert alert-danger">An error occurred. Please try again.</div>');
                    },
                    complete: function() {
                        // Re-enable submit button
                        submitBtn.prop('disabled', false).html('Send Message');
                    }
                });
            });
        });
    </script>
